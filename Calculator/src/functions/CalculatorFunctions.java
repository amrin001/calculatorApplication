package functions;

public class CalculatorFunctions {
	
		public double additionFunction(double number1, double number2 ) {
			double sum = number1 + number2;
			return sum;
		}
		
		public double subtractionFunction(double number1, double number2 ) {
			double difference = number1 - number2;
			return difference;
		}
		
		public double multiplyFunction(double number1, double number2 ) {
			double product = number1 * number2;
			return product;
		}
		
		public double divideFunction(double number1, double number2 ) {
			double divide = number1 / number2;
			return divide;
		}

}
